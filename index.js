String.prototype.냐 = String.prototype

String.prototype.냐하 = () => {
  process.exit()
}

String.prototype.냐핫 = (문장이다냐) => {
  console.log(문장이다냐)
  return String.prototype
}

String.prototype.냐앗 = eval

String.prototype.냐아 = true

String.prototype.냐냐 = false

String.prototype.끄앙 = '!'
